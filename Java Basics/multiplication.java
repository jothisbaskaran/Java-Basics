package practicejava;

import java.util.Scanner;

public class multiplication {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		int a=sc.nextInt();
		int b=sc.nextInt();
		int mul=0;
		for(int i=1;i<=a;i++);
		{ mul=mul+b;
	}
System.out.println(mul);
}
}